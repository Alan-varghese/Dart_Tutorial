// switch Statements

void main() {
  int n = 3;
  print(Function(n));
}

String Function(int n) {
  switch (n) {
    case (1):
      return "Monday";

    case (2):
      return "Tuesday";

    case (3):
      return "wednesday";

    case (4):
      return "Thursday";

    case (5):
      return "friday";

    default:
      return "Record not found";
  }
}
